					<p>Prices for outdoor stairlifts depend on your particular requirements and the configuration of your steps. This is why we recommend you arrange a free, no obligation quote from a reputable manufacturer. A surveyor will need to visit to ascertain which type of stairlift will be most appropriate for your circumstances and provide you with a written quotation for the work.</p>
					<p>As with indoor models, straight stairlifts are cheaper because they can be installed on readymade tracks. Curved ones are more expensive because they require a track or rail to be made to measure.</p>
